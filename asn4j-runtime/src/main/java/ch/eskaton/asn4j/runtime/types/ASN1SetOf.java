package ch.eskaton.asn4j.runtime.types;

import ch.eskaton.asn4j.runtime.annotations.ASN1Tag;

@ASN1Tag(clazz = ASN1Tag.Clazz.Universal, tag = 17, mode = ASN1Tag.Mode.Explicit, constructed = true)
public class ASN1SetOf<T extends ASN1Type> extends ASN1CollectionOf<T> {

    @Override
    public int hashCode() {
        return 31 + ((values == null) ? 0 : values.hashCode());
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }

        if (obj == null) {
            return false;
        }

        if (!(obj instanceof ASN1SetOf)) {
            return false;
        }
        @SuppressWarnings("rawtypes")

        ASN1SetOf other = (ASN1SetOf) obj;

        if (values == null) {
            if (other.values != null) {
                return false;
            }
        } else if (!values.equals(other.values)) {
            return false;
        }

        return true;
    }

}
